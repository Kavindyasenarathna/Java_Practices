public class AssignmentOperators {

    public static void main (String[]args){

        int a = 2;

        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(a+=1);
        System.out.println(a-=1);
        System.out.println(a*=4);
        System.out.println(a/=6);
        System.out.println(-a);

    }
    
}
