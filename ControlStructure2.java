public class ControlStructure2 {

    public static void main (String []aegs){

    //summation of all the numbers from 1 to 10//

    //using for loop//

    System.out.println("");

    int sum=0;

    for(int i=1; i<=10; i++){
        sum+=i;
    }
    System.out.println("the sum of numbers from 1 to 10="+sum);

    System.out.println("");

    //using while loop//

    int sum=0;
    int x=1;

    while(x<=10){
        sum+=x;
        x++;
    }
    System.out.println("the sum of numbers from 1 to 10="+sum);





} 
}
